-- CONSTRAINT YOO FOREIGN KEY(eidos_ripou) REFERENCES ripos(eidos) ON DELETE CASCADE ON UPDATE CASCADE,
CONSTRAINT YOOO FOREIGN KEY(kodikos) REFERENCES ripos(kod_stathmou) ON DELETE CASCADE ON UPDATE CASCADE
);

