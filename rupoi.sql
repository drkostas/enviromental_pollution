CREATE DATABASE IF NOT EXISTS rupoi;
USE rupoi;

DROP TABLE IF EXISTS stathmos;
CREATE TABLE IF NOT EXISTS stathmos(
kodikos VARCHAR(25),
onoma VARCHAR(25),
geogr_mikos FLOAT( 10, 6 ) NOT NULL,
geogr_platos FLOAT( 10, 6 ) NOT NULL,
PRIMARY KEY(kodikos)
);

DROP TABLE IF EXISTS ripos;
CREATE TABLE IF NOT EXISTS ripos(
kod_stathmou VARCHAR(25),
eidos VARCHAR(25),
PRIMARY KEY(kod_stathmou),
CONSTRAINT YO FOREIGN KEY(kod_stathmou) REFERENCES stathmos(kodikos) ON DELETE CASCADE ON UPDATE CASCADE
);

